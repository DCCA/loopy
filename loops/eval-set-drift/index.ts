import type {
  ActResult,
  DetectResult,
  FileChange,
  Guardrails,
  Loop,
  LoopManifest,
  StateStore,
  Trigger,
} from "../../src/core/index.js";
import { normalizeCategory, uncovered, type DriftSource } from "./hooks/drift.js";

const SURFACED_KEY = "eval-set-drift:surfaced";

export interface DriftConfig {
  /** path to the rendered drift report */
  reportPath: string;
}

export interface DriftServices {
  source: DriftSource;
  state: StateStore;
}

/** The fully-computed picture for one run, derived without persisting anything. */
interface DriftPicture {
  /** all uncovered production categories this run */
  u: string[];
  /** already-surfaced categories loaded from state */
  surfaced: string[];
  /** uncovered categories not yet surfaced — the proposed new eval cases */
  fresh: string[];
}

export function createEvalSetDriftLoop(
  config: DriftConfig,
  guardrails: Guardrails,
  trigger: Trigger,
  services: DriftServices,
): Loop {
  /** Compute the picture WITHOUT saving state; both detect and act use it. */
  async function computePicture(): Promise<DriftPicture> {
    const evalCats = await services.source.evalCategories();
    const prodCats = await services.source.productionCategories();
    const u = uncovered(evalCats, prodCats);

    const surfaced = (await services.state.load<string[]>(SURFACED_KEY)) ?? [];
    const surfacedSet = new Set(surfaced.map((c) => normalizeCategory(c)));
    const fresh = u.filter((c) => !surfacedSet.has(c));

    return { u, surfaced, fresh };
  }

  return {
    id: "eval-set-drift",
    trigger,
    guardrails,

    async detect(): Promise<DetectResult> {
      const { fresh } = await computePicture();
      if (fresh.length === 0) {
        return { workNeeded: false, reason: "no uncovered eval categories" };
      }
      return {
        workNeeded: true,
        reason: `${fresh.length} uncovered eval categor${fresh.length === 1 ? "y" : "ies"}`,
        affected: fresh,
      };
    },

    async act(): Promise<ActResult> {
      const { surfaced, fresh } = await computePicture();

      // Record the newly-surfaced categories so the next run won't re-report them.
      const nextSurfaced = Array.from(
        new Set([...surfaced.map((c) => normalizeCategory(c)), ...fresh]),
      ).sort();
      await services.state.save<string[]>(SURFACED_KEY, nextSurfaced);

      const dateIso = new Date().toISOString().slice(0, 10);
      const changes: FileChange[] = [
        {
          path: config.reportPath,
          op: "write",
          contents: renderDriftReport(fresh, dateIso),
        },
      ];

      return { changes, summary: summarize(fresh) };
    },
  };
}

export function createEvalSetDriftLoopFromManifest(
  manifest: LoopManifest,
  services: DriftServices,
): Loop {
  const c = manifest.config;
  const config: DriftConfig = {
    reportPath:
      typeof c["reportPath"] === "string" ? c["reportPath"] : "reports/eval-drift.md",
  };
  return createEvalSetDriftLoop(config, manifest.guardrails, manifest.trigger, services);
}

/** Render the deterministic drift report listing the proposed new eval cases. */
export function renderDriftReport(newCats: string[], dateIso: string): string {
  const lines: string[] = [
    "# Eval-set drift",
    "",
    `_Generated ${dateIso} by loopy eval-set-drift._`,
    "",
    `Uncovered production categories: ${newCats.length}`,
    "",
  ];

  if (newCats.length === 0) {
    lines.push("_(no uncovered categories)_");
  } else {
    lines.push("Proposed new eval cases:");
    lines.push("");
    for (const c of newCats) {
      lines.push(`- \`${c}\``);
    }
  }

  lines.push("");
  return lines.join("\n");
}

function summarize(fresh: string[]): string {
  return [
    "## Eval-set drift",
    "",
    "Production categories not represented in the eval set:",
    ...fresh.map((c) => `- \`${c}\``),
    "",
    "_Generated by loopy eval-set-drift. Add the uncovered cases to your eval set (review for PII)._",
  ].join("\n");
}

export type { DriftSource } from "./hooks/drift.js";
export { normalizeCategory, uncovered } from "./hooks/drift.js";
